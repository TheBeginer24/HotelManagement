package Model;

/**
 *
 * @author ADMIN
 */
public class User {
    private int id,role;
    private String name,pass;

    public User() {
    }

    public User(int id, String name, String pass, int role) {
        this.id = id;
        this.role = role;
        this.name = name;
        this.pass = pass;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
    
    
}
